    // Minimal stub definitions that satisfy the generator's semantic checks.
    private const string SupportSource = """
        namespace TestConsoleApp.Application.Abstractions
        {
            public interface IMenuCommand
            {
                string Title { get; }
                System.Threading.Tasks.Task ExecuteAsync(System.Threading.CancellationToken cancellationToken = default);
            }

            [System.AttributeUsage(System.AttributeTargets.Class)]
            public sealed class SubMenuAttribute : System.Attribute
            {
                public SubMenuAttribute(params string[] path) { }
                public string[] Path { get; }
            }

            [System.AttributeUsage(System.AttributeTargets.Class)]
            public sealed class HotkeyAttribute : System.Attribute
            {
                public HotkeyAttribute(char key, System.ConsoleModifiers modifiers = default) { }
                public char Key { get; }
                public System.ConsoleModifiers Modifiers { get; }
            }

            public static class CommandRegistry
            {
                public static void Register(IMenuCommand command) { }
                public static void RegisterHotkey(char key, System.ConsoleModifiers modifiers, IMenuCommand command) { }
            }
        }

        namespace TestConsoleApp.Presentation.Menus
        {
            public sealed class SubMenuCommand : TestConsoleApp.Application.Abstractions.IMenuCommand
            {
                public SubMenuCommand(string title, System.Collections.Generic.IReadOnlyList<TestConsoleApp.Application.Abstractions.IMenuCommand> commands) { }
                public string Title => "";
                public System.Threading.Tasks.Task ExecuteAsync(System.Threading.CancellationToken cancellationToken = default)
                    => System.Threading.Tasks.Task.CompletedTask;
            }
        }
        """;

    private static (ImmutableArray<Diagnostic> Diagnostics, ImmutableArray<GeneratedSourceResult> Sources)
        RunGenerator(string testSource)
    {
        var references = ((string?)AppContext.GetData("TRUSTED_PLATFORM_ASSEMBLIES"))!
            .Split(Path.PathSeparator)
            .Select(p => (MetadataReference)MetadataReference.CreateFromFile(p));

        var compilation = CSharpCompilation.Create(
            "TestAssembly",
            [CSharpSyntaxTree.ParseText(SupportSource), CSharpSyntaxTree.ParseText(testSource)],
            references,
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

        GeneratorDriver driver = CSharpGeneratorDriver.Create(new CommandRegistrationGenerator().AsSourceGenerator());
        driver = driver.RunGenerators(compilation);

        var result = driver.GetRunResult().Results[0];
        return (result.Diagnostics, result.GeneratedSources);
    }

